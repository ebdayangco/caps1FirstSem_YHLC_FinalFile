    private void displaybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displaybtnActionPerformed
             
          if(!resultTable.getSelectionModel().isSelectionEmpty()){
              
              if(this.currentOpen.equals("student")){
                  
                viewStudentInfo.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR)); 
                this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));   
                int row = resultTable.getSelectedRow();
                int id = Integer.parseInt(resultTable.getValueAt(row, 0).toString());
                Student student = this.controller.getStudent(id);
                viewStudentInfo.setDisplayedStudent(student);
                viewStudentInfo.displayData();
                viewStudentInfo.displayGuardians();
                viewStudentInfo.getSlider().exitSlider();
                viewStudentInfo.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)); 
                this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)); 
                
                
                this.controller = new StudentController();
                
                CashierStudentInfoMain.setDisplayedStudent(this.controller.getStudent(student.getStudentID()));
                
              }
              else{
                  
                addTransaction.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR)); 
                this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR)); 
                int row = resultTable.getSelectedRow();
                int id = Integer.parseInt(resultTable.getValueAt(row, 0).toString());
                Student student = this.controller.getStudent(id); 
           
                addTransaction.setCurrentStudentTransacted(student);
                addTransaction.setAllLatestTransaction();
                addTransaction.displayStudentInfo();
                
                addTransaction.getSlider().exitSlider();
                addTransaction.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)); 
                this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)); 
              }
                
          }
         
    }//GEN-LAST:event_displaybtnActionPerformed

