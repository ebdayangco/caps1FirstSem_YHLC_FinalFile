    public void displayEditStudent(){
        
        Student editStudent = viewStudentinfo.getDisplayedStudent();
        
        if(editStudent !=null){
            
           
            this.controller.displayUploadImage(imageDisplayer, editStudent.getImage());
            schoolID_comp.setText(editStudent.getSchoolID());
            fullname_comp.setText(editStudent.getFullname());
            grade_comp.setSelectedItem(editStudent.getLevel());
            contactNum_comp.setText(editStudent.getContactNumber());
            address_comp.setText(editStudent.getAddress());
            form138_comp.setSelected(editStudent.isForm138());
            nso_comp.setSelected(editStudent.isBirthCertificate());
            this.controller.setUploadImage(viewStudentinfo.getDisplayedStudent().getImage());
    }
    
    }

    public Student getEditStudent() {
        
        Student editStudent = viewStudentinfo.getDisplayedStudent();
        
        editStudent.setSchoolID(schoolID_comp.getText());
        editStudent.setFullname(fullname_comp.getText());
        editStudent.setLevel(grade_comp.getSelectedItem().toString());
        editStudent.setContactNumber(contactNum_comp.getText().isEmpty()?"No Contact Number":contactNum_comp.getText());
        editStudent.setAddress(address_comp.getText().isEmpty()?"No Address":address_comp.getText());
        editStudent.setImage(this.controller.getUploadImage());
        editStudent.setForm138(form138_comp.isSelected());
        editStudent.setBirthCertificate(nso_comp.isSelected());
       
        
        return editStudent;
    }
    
     public void clearAll(){
        
        //clearing textfield and textarea
        schoolID_comp.setText("");
        fullname_comp.setText("");
        contactNum_comp.setText("");
        address_comp.setText("");
        
        //set combobox on the first item
        grade_comp.setSelectedIndex(1);
        
        //uncheck checbox form form138 and nso/birthcertificate
        form138_comp.setSelected(false);
        nso_comp.setSelected(false);
        
        //clear image or icon in JLabel
        imageDisplayer.setIcon(null);
        imageDisplayer.revalidate();
        
        
        this.viewStudentinfo.displayData();
        
        
    }
     
     public boolean validateInput(){
         
         
        Student student = getEditStudent();
        
        String displayMessage = "";
        
        if(student.getSchoolID().isEmpty()){
            displayMessage+="Please input the SchoolID.\n";
        }
        if(student.getFullname().isEmpty()){
            displayMessage+="Please input the Fullname.\n";
        }
        
        
        setMessage(displayMessage);
        return student.getSchoolID().isEmpty() ||
                student.getFullname().isEmpty();
    }
    




